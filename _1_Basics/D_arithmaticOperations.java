package _1_Basics;
public class D_arithmaticOperations {
	public static void main(String[] args) {
            int sum=10+3;
            System.out.println(sum);

            int sub=10-3;
            System.out.println(sub);
            
            int mul=10*3;
            System.out.println(mul);

            double div=(double)10/3;//type casting
            System.out.println(div);   
	}
}

